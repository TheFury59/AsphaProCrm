function tc({ content, widthDxa, fill, bold, mono, alignment, color, size, span }) {
  let runs;
  if (Array.isArray(content)) runs = content;
  else runs = [text(String(content ?? ""), { bold, font: mono ? MONO : FONT, color, size })];
  return new TableCell({
    width: { size: widthDxa, type: WidthType.DXA },
    borders: allBorders,
    shading: fill ? { fill, type: ShadingType.CLEAR } : undefined,
    margins: cellMargin,
    columnSpan: span,
    children: [new Paragraph({ alignment, children: runs })],
  });
}

function table(headers, rows, widths) {
  const total = widths.reduce((a, b) => a + b, 0);
  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((h, i) =>
      tc({ content: h, widthDxa: widths[i], fill: C.primary, bold: true, color: "FFFFFF" })),
  });
  const bodyRows = rows.map((row) =>
    new TableRow({
      children: row.map((cell, i) => tc({ content: cell, widthDxa: widths[i], size: 20 })),
    }));
  return new Table({ width: { size: total, type: WidthType.DXA }, columnWidths: widths, rows: [headerRow, ...bodyRows] });
}

